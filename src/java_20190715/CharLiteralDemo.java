 package java_20190715;

public class CharLiteralDemo {
	public static void main(String[] args) {
		char c1= '\uCD5C';
		char c2= '\uD0CC';
		char c3= '\uC601';
			
		System.out.println(c1);
		System.out.println(c2);
		System.out.println(c3);
		
		//char literal의 문자현 => ''
		
		char c5 = 'q';
		
		char c6 = '\'';
		
		char c7 = '\\';
		
		
		String path = "aa\ncc";
		System.out.println(path);
		
	}

}
